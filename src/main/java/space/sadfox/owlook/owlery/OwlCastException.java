package space.sadfox.owlook.owlery;

public class OwlCastException extends OwlException {

	private static final long serialVersionUID = -5689446394223438840L;

	public OwlCastException() {
		super();
	}

	public OwlCastException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public OwlCastException(String message, Throwable cause) {
		super(message, cause);
	}

	public OwlCastException(String message) {
		super(message);
	}

	public OwlCastException(Throwable cause) {
		super(cause);
	}
	
	

}
