package space.sadfox.owlook.ui.base;

public class FXMLControllerException extends ControllerException {

  public FXMLControllerException() {
  }

  public FXMLControllerException(String message) {
    super(message);
  }

  public FXMLControllerException(Throwable cause) {
    super(cause);
  }

  public FXMLControllerException(String message, Throwable cause) {
    super(message, cause);
  }

  public FXMLControllerException(String message, Throwable cause, boolean enableSuppression,
      boolean writableStackTrace) {
    super(message, cause, enableSuppression, writableStackTrace);
  }

}
